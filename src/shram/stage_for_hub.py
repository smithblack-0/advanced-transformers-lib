def comment_out_type_checking(source: str) -> str:
    """
    Walks over the source lines, and identifies any
    cases that are just for type hints. If one is found
    it comments it out. Commenting out continues until exiting
    the indentation level
    :param lines: The lines of the source file to check
    :return: The revised lines
    """
    result = []
    in_block = False
    expected_indent = None
    lines = source.split("\n")
    for line in lines:
        if line.startswith("if") and "TYPE_CHECKING" in line:
            # Start new block.
            in_block = True
            expected_indent = None
        elif in_block:
            # Check indent. Capture if not set, or reset if
            # we are lower than target.
            indent = len(line) - len(line.lstrip())
            if indent == 0 and line.strip():
                in_block = False
            elif indent != 0:
                if expected_indent is None:
                    expected_indent = indent
                elif indent < expected_indent:
                    in_block = False

        # If in block, comment it out.
        if in_block:
            line = "# " + line
        result.append(line)

    return "\n".join(result)

def resolve_comments_to_sentinels(source: str) -> Tuple[str, Dict[str, str]]:
    """
    Resolves source code into a proxy of
    a dictionary of comment sentinels, and
    a string with those sentinels standing in for the
    comments. Multiline docstrings and regular comments
    are captured and encoded this way. Substituting
    the sentinels back in reconstructs the string, and
    removing comments prevents triggering on their content.
    :param source: The source to parse from
    :return:
        - The sentinel-filled source
        - The dictionary of sentinel keys to representations.
    """
    sentinels: Dict[str, str] = {}
    counter = 0
    pattern = re.compile(r'(""".*?"""|\'\'\'.*?\'\'\'|#[^\n]*)', re.DOTALL)

    def replace(match: re.Match) -> str:
        nonlocal counter
        key = f"__COMMENT_{counter}__"
        sentinels[key] = match.group(0)
        counter += 1
        return key

    result = pattern.sub(replace, source)
    return result, sentinels

def resolve_import_blocks_to_sentinels(source: str) -> Tuple[str, Dict[str, str]]:
    """
    Resolves top-level import statements into sentinel representations.
    Multiline parenthesized imports are captured as a single block.
    Comments within import lines are promoted above the sentinel in the
    result, preserving them without polluting the import content.
    :param source: The source to parse from, with comments already sentineled.
    :return:
        - The sentinel-filled source
        - The dictionary of sentinel keys to clean import representations.
    """
    sentinels = {}
    counter = 0
    lines = source.split("\n")
    result = []
    it = iter(lines)  # shared iterator allows inner loop to consume continuations from the same position

    for line in it:
        if line.startswith("import ") or line.startswith("from "):
            block = [line]
            if "(" in line:
                # parenthesized import — consume lines from the shared iterator until
                # the block closes, so the outer loop doesn't see them as separate lines
                for continuation in it:
                    block.append(continuation)
                    if ")" in continuation:
                        break

            # strip comment sentinels from block content, collecting them for promotion above the import
            promoted = []
            clean_lines = []
            for block_line in block:
                comments = re.findall(r'__COMMENT_\d+__', block_line)
                promoted.extend(comments)
                clean = re.sub(r'__COMMENT_\d+__', '', block_line).rstrip()
                if clean:
                    clean_lines.append(clean)

            key = f"__IMPORT_{counter}__"
            sentinels[key] = "\n".join(clean_lines)
            counter += 1
            result.extend(promoted)
            result.append(key)
        else:
            result.append(line)

    return "\n".join(result), sentinels
def standardize_import_blocks(sentinels: Dict[str, str]) -> Dict[str, str]:
    """
    Standardizes import blocks to canonical single-line forms.
    Semicolon-separated, comma-separated, and parenthesized imports
    are all split into individual import statements.
    :param sentinels: The import sentinel dict from resolve_import_blocks_to_sentinels
    :return: The same dict with values replaced by standardized import lines.
    """
    result = {}
    for key, block in sentinels.items():
        lines = []

        # Statements involving ";" are split then handled separately.
        for statement in block.split(";"):
            # Break statement into a reusable prefix and a suffix,
            # with one statement or possibly many.
            # Prefix contains the base such as "from . import"
            # Suffix contains remaining statemetns.
            statement = statement.strip()
            prefix, suffix = statement.split("import")
            prefix = prefix + "import"

            #Standardize multicomma statements to exist on a single
            # line.
            if "(" in suffix and ")" in suffix:
                multiline_section = re.search(r'\((.+?)\)', suffix, re.DOTALL)
                replacement_key = multiline_section.group(0)
                contents = multiline_section.group(1)
                contents = contents.replace("\n", "")
                suffix = suffix.replace(replacement_key, contents)

            # Split on commas, strip whitespace, and make statements.
            #
            # In cases where only one statement is needed, we just
            # reconstructed the same one.
            for operand in suffix.split(","):
                operand = operand.strip()
                if len(operand) == 0:
                    # Skips comment only cases.
                    continue
                if operand == "*":
                    raise ValueError(
                        f"Star imports are not supported by the inliner: '{prefix} *'"
                    )

                statement = prefix + " " + operand
                lines.append(statement)

        result[key] = "\n".join(lines)
    return result

def validate_source(sentinel_string):
    """
    If properly handled, nothing should
    say import anymore
    :param sentinel_string:
    """
    lines = sentinel_string.split("\n")
    for i, line in enumerate(lines):
        if "import" in line:
            msg = f"Unsupported import at line {i}\n"
            msg += f"line was: {line}"
            raise ValueError(msg)

def restore_sentinels(source: str, sentinels: Dict[str, str]) -> str:
    """Substitute sentinel keys back into source, restoring original comments."""
    for key, value in sentinels.items():
        source = source.replace(key, value)
    return source



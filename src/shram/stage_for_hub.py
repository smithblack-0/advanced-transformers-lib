def _resolve_relative_path(module: str, base_dir: Path, import_name: str) -> Path:
    """Resolve a relative import module string to an absolute .py file path.

    Counts the leading dots to determine how many directories to ascend from
    base_dir, then follows the remaining dotted path. When the module string
    contains only dots and no path (bare-dot form, e.g. ``from . import X``),
    import_name is used to locate the target file.

    Resolution order for each candidate location:
      1. <resolved_dir>/<path>.py
      2. <resolved_dir>/<path>/__init__.py

    Args:
        module: Relative module string, e.g. ``.cache.shram_cache`` or ``..``.
        base_dir: Directory of the importing file.
        import_name: The name being imported; used only for bare-dot forms where
            the module string has no path component after the leading dots.

    Returns:
        Absolute path to the resolved .py file.

    Raises:
        FileNotFoundError: If no matching .py file exists at either candidate.
    """
    dots = 0
    for c in module:
        if c == ".":
            dots += 1
        else:
            break

    remainder = module[dots:]

    current = base_dir
    for _ in range(dots - 1):
        current = current.parent

    if remainder:
        parts = remainder.split(".")
        candidate = current.joinpath(*parts).with_suffix(".py")
        if candidate.is_file():
            return candidate
        candidate = current.joinpath(*parts, "__init__.py")
        if candidate.is_file():
            return candidate
    else:
        # Bare-dot form: from . import X.
        # If X resolves to a file, the caller is importing a module as a
        # namespace object (e.g. model.ShramModel). That pattern breaks after
        # inlining because X no longer exists as a name. Raise explicitly.
        candidate = current / (import_name + ".py")
        if candidate.is_file():
            raise ValueError(
                f"Module-as-namespace import 'from {module} import {import_name}' "
                f"is not supported by the inliner. "
                f"Use 'from .{import_name} import SpecificName' instead."
            )
        candidate = current / import_name / "__init__.py"
        if candidate.is_file():
            raise ValueError(
                f"Module-as-namespace import 'from {module} import {import_name}' "
                f"is not supported by the inliner. "
                f"Use 'from .{import_name} import SpecificName' instead."
            )

    raise FileNotFoundError(
        f"Cannot resolve relative import '{module}' "
        f"(name={import_name!r}) from {base_dir}"
    )


def resolve_key(statement: str, base_dir: Path) -> str:
    """Derive the canonical deduplication key for an import statement.

    Relative imports produce a ``rel:<posix-path>`` key using the resolved
    absolute file path. External imports produce an ``abs:<statement>`` key
    using the import statement text. Both forms are stored in the shared seen
    set by resolve_import.

    Args:
        statement: A canonical single-line import statement.
        base_dir: Directory of the file containing the statement; used to
            resolve relative import paths.

    Returns:
        Canonical string key for use in the seen set.
    """
    parts = statement.split()

    if parts[0] == "from" and parts[1].startswith("."):
        # Relative import. import_name is the symbol after the "import" keyword.
        import_name = parts[3] if len(parts) > 3 else ""
        resolved = _resolve_relative_path(parts[1], base_dir, import_name)
        return f"rel:{resolved.as_posix()}"

    return f"abs:{statement}"


def resolve_import(key: str, seen: set[str], _external: list[str] | None = None, source_dir: Path | None = None) -> str:
    """Resolve an import key to its substitution string.

    On first encounter of a relative import, marks the key as seen and returns
    the inlined file content preceded by a section header identifying the source
    file. On first encounter of an external import, appends the statement to
    _external (if provided) and returns empty string so the statement is emitted
    at the top of the merged file rather than inline. On subsequent encounters
    of either kind, returns empty string — relative imports are already inlined,
    and external imports are already collected.

    When source_dir is provided, absolute imports that resolve to files under
    source_dir raise ValueError immediately — they are local source references
    disguised as external imports and would produce broken merged output.

    Args:
        key: Canonical key from resolve_key.
        seen: Shared set of already-processed keys; mutated by this call.
        _external: Shared list for collecting external import statements;
            populated on first encounter of each abs: key. When None, external
            imports are emitted inline (backward-compatible path).
        source_dir: Source directory of the staged model; passed to
            _is_module_in_directory to detect local absolute imports. When
            None, local import detection is skipped.

    Returns:
        Substitution string for the sentinel.
    """
    if key in seen:
        return ""

    seen.add(key)

    if key.startswith("rel:"):
        header = f"# -----------\n# Inlined from: {Path(key[4:]).name}\n# -----------"
        return header + "\n" + inline_imports(Path(key[4:]), seen, _external, source_dir)

    # External import: collect for top-of-file emission.
    if _external is not None:
        statement = key[4:]
        if source_dir is not None:
            top_level = statement.split()[1].split(".")[0]
            if _is_module_in_directory(top_level, source_dir):
                raise ValueError(
                    f"Absolute import resolves to a local source file and must be "
                    f"converted to a relative import: {statement}"
                )
        _external.append(statement)
        return ""
    return key[4:]


# ---------------------------------------------------------------------------
# Inlining orchestration
# ---------------------------------------------------------------------------

def inline_imports(path: Path, seen: set[str] | None = None, _external: list[str] | None = None, source_dir: Path | None = None) -> str:
    """Inline all imports in a Python file, recursively following relative imports.

    Processes the file through the normalization pipeline, resolves each import
    sentinel, and performs string substitution to produce the merged source.
    Relative imports are replaced with the full recursively inlined content of
    the imported file, preceded by a section header. External imports are
    collected into _external and emitted once at the top of the merged file via
    an __IMPORT_ABS__ sentinel, which is inserted after the module docstring on
    the top-level call and replaced with the collected statements at the end.

    Args:
        path: Absolute path to the Python source file to inline.
        seen: Shared set of already-processed keys. An empty set is created if
            None; callers may pass an existing set to share deduplication state
            across the recursive call chain.
        _external: Shared list for collecting external import statements. Created
            at the top-level call and passed through recursive calls. When None,
            this is the top-level call.
        source_dir: Source directory of the staged model. Captured from
            path.parent at the top-level call and threaded through recursive
            calls so resolve_import can detect local absolute imports.

    Returns:
        Merged Python source with all relative imports inlined.

    Raises:
        ValueError: If unsupported import constructs are found, with the source
            file path included in the message.
    """
    if seen is None:
        seen = set()

    is_top_level = _external is None
    if is_top_level:
        _external = []
        source_dir = path.parent

    source = path.read_text(encoding="utf-8")

    try:
        source = comment_out_type_checking(source)
        source, comment_sentinels = resolve_comments_to_sentinels(source)

        if is_top_level:
            match = re.search(r'__COMMENT_\d+__', source)
            if match:
                source = source[:match.end()] + "\n__IMPORT_ABS__" + source[match.end():]
            else:
                source = "__IMPORT_ABS__\n" + source

        source, import_sentinels = resolve_import_blocks_to_sentinels(source)
        import_sentinels = standardize_import_blocks(import_sentinels)
        validate_source(source)
    except ValueError as e:
        raise ValueError(f"{path}: {e}") from e

    for sentinel, block in import_sentinels.items():
        parts = []
        for line in block.splitlines():
            line = line.strip()
            if not line:
                continue
            key = resolve_key(line, path.parent)
            replacement = resolve_import(key, seen, _external, source_dir)
            if replacement:
                parts.append(replacement)
        source = source.replace(sentinel, "\n".join(parts))

    source = restore_sentinels(source, comment_sentinels)

    if is_top_level:
        source = source.replace("__IMPORT_ABS__", "\n".join(_external))

    return source


# ---------------------------------------------------------------------------
# Staging
# ---------------------------------------------------------------------------

def stage_model(source_dir: Path, dest_dir: Path) -> None:
    """Flatten source_dir into dest_dir as a single merged huggingface.py.

    Inlines all Python code reachable from source_dir/huggingface.py into a
    single dest_dir/huggingface.py. The merged file begins with a generated-file
    header, followed by all external imports collected at the top after the
    module docstring, then the inlined code with section headers marking each
    source file's contribution. Non-Python files are copied unchanged.
    dest_dir must already exist and should be empty.

    Args:
        source_dir: Source model directory (e.g. src/shram/model/).
        dest_dir: Destination staging directory to write the merged file into.
    """
    merged = inline_imports(source_dir / "huggingface.py")
    merged = _GENERATED_HEADER + "\n" + merged
    (dest_dir / "huggingface.py").write_text(merged, encoding="utf-8")

"""Produce a Hub-compatible staging directory from a model source directory.

HuggingFace's remote from_pretrained recursively resolves all imports from the
entry-point file into the module cache. Local from_pretrained does not — only
files directly imported from huggingface.py are fetched, causing a crash when
the model tries to load second-level dependencies. The only correct fix is to
inline all model code into a single file before upload.

**What stage() produces**

stage(source_dir, dest_dir) walks source_dir, inlines all Python model code
reachable from source_dir/huggingface.py into a single dest_dir/huggingface.py,
and copies non-Python files (configs, tokenizer files, etc.) into dest_dir
unchanged. The result is a directory from which AutoModelForCausalLM.from_pretrained
with local_files_only=True succeeds.

**How the inliner works**

Naive line-by-line import detection is defeated by multiline parenthesized
imports, inline comments, docstrings containing the word "import", and
TYPE_CHECKING blocks. The inliner therefore operates through a sentinel
pipeline:

1. TYPE_CHECKING blocks are converted to comments so their imports are inert.
2. Docstrings and comments are extracted and replaced with __COMMENT_N__
   sentinels, preventing their content from being misread as live imports.
3. Top-level import blocks — including parenthesized multiline forms — are
   extracted and replaced with __IMPORT_N__ sentinels. Inline comments are
   promoted above the sentinel.
4. Each import block is standardized into canonical single-line forms. The
   six supported forms are:
       import _module_
       import _module_ as _alias_
       from _module_ import _name_
       from _module_ import _name_ as _alias_
       from ._relative_ import _name_
       from ._relative_ import _name_ as _alias_
   Multi-name, parenthesized, and semicolon-separated imports are all expanded
   into these forms. Star imports raise ValueError.
5. Each __IMPORT_N__ sentinel is resolved: relative imports are replaced with
   the recursively inlined content of the target file; external imports are
   emitted on first encounter and commented out on recurrence.
6. __COMMENT_N__ sentinels are restored, returning docstrings and comments to
   the merged source verbatim.
"""

import importlib.util
import shutil
import re
from pathlib import Path
from typing import Dict, Tuple

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

_GENERATED_HEADER = (
    "# This file is auto-generated by stage_for_hub.py from the source repository.\n"
    "# Do not edit it directly — changes will be overwritten on the next release.\n"
)

# ---------------------------------------------------------------------------
# Validation helpers
# ---------------------------------------------------------------------------

def _is_module_in_directory(module_name: str, source_dir: Path) -> bool:
    """Return True if module_name resolves to a file under source_dir.

    Uses Python's import machinery to locate where the module lives on disk,
    then checks whether that location falls under source_dir. A True result
    means the import references a local source file rather than an installed
    external package — the caller should raise, because the merged output would
    reference a file that does not exist in the staging environment.

    Raises ValueError rather than returning a default if the module is missing
    from the environment or has no concrete file origin: either condition means
    staging cannot proceed correctly and should crash immediately.

    Args:
        module_name: Top-level module name, e.g. ``torch`` or ``transformers``.
        source_dir: Directory to check against; the directory containing the
            staged huggingface.py entry point.

    Returns:
        True if the module's origin file is under source_dir, False otherwise.

    Raises:
        ValueError: If the module is not found in the current environment or
            has no concrete file origin (e.g. a namespace package).
    """
    spec = importlib.util.find_spec(module_name)
    if spec is None:
        raise ValueError(
            f"Module '{module_name}' not found in the current environment; "
            f"all model dependencies must be installed in the staging environment."
        )
    if spec.origin is None:
        raise ValueError(
            f"Module '{module_name}' has no concrete file origin (namespace package); "
            f"convert any absolute imports of this module to relative imports."
        )
    return Path(spec.origin).is_relative_to(source_dir)


